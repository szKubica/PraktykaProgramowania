"""
This module contains the utility functions for the calculator app.
"""


def add(a: int, b: int) -> int:
    """
    Dodawanie
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Odejmowanie"""
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Mnozenie"""
    return a * b


def divide(a: int, b: int) -> float:
    """
    Dzielenie"""
    return a / b
