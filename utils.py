"""
Kalkulator
"""


def add(a: int, b: int) -> int:
    """
    Dodawanie liczb
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Odejmowanie liczb
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Mnozenie liczb
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Dzielenie liczb
    """
    return a / b